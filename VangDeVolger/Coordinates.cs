namespace VangDeVolger
{
    public struct Coordinates
    {
        public int X;
        public int Y;

        /// <summary>
        /// Coordinates Struct
        /// </summary>
        /// <param name="x">X Position</param>
        /// <param name="y">Y Position</param>
        public Coordinates(int x, int y)
        {
            X = x;
            Y = y;
        }
    }
}
